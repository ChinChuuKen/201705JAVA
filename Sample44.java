class Sample44
{
	public static void main(String[] args)
	{
		int num1=10;
		int num2=3;
		
		System.out.println("num1とnum2にているな演算を行います。");
		System.out.println("num1+num2は"+(num1+num2)+"です。");
		System.out.println("num1-num2は"+(num1-num2)+"です。");
		System.out.println("num1*num2は"+(num1*num2)+"です。");
		System.out.println("num1/num2は"+(num1/num2)+"です。");
		System.out.println("num1%num2は"+(num1%num2)+"です。");
		
		byte i=8;
		System.out.println("iは"+i+"です。");
		System.out.println("iの補数は"+~i+"です。");
}
}
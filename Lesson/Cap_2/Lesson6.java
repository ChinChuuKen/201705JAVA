class Lesson6
{
	public static void main(String[] args)
	{
		System.out.println("10進数の20を表示:"+20);
		System.out.println("8進数の20を表示:"+020);
		System.out.println("16進数の20を表示:"+0x20);
		
		
		System.out.println("10進数を使い、10進数の20を表示:"+20);
		System.out.println("8進数を使い、10進数の20を表示:"+ 024 );
		System.out.println("16進数を使い、10進数の20を表示:"+ 0x14 );
	}
}
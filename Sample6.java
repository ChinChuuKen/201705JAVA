class Sample6
{
    public static void main(String[] args)
	   {
	   System.out.println("10進数の10は"+10+"です");
	   System.out.println("8進数の10は"+010+"です");
	   System.out.println("16進数の10は"+0x10+"です");
	   System.out.println("16進数のFは"+0xF+"です");
	    
	    System.out.println("10進数の10-1は"+(10-1)+"です");
	   	//8進数から10進数を  する事も可能
	   System.out.println("8進数の10-1は"+(010-1)+"です");
	   	//16進数から10進数を思案する事も可能
	   System.out.println("16進数の10-1は"+(0x10-1)+"です");
	   	//8進数から16進数を思案する事も可能
	   System.out.println("8進数の10+16は"+(010+16)+"です");
    }
}



package Exersice;

public class Exersice7x {
	public static void main(String[] args) {
		for(int i=0;i<6;i++){
			for(int j=0;j<i;j++){
			System.out.print("*");
			}
			System.out.println();
		}
		for(int x=4;x>0;x--){
			for(int y=x;y>0;y--){
				System.out.print("*");
			}
			System.out.println();
		}
		
	}
}
